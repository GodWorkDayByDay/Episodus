#include "ns_ob1/OB1Edge.h"

long OB1Edge::_lObjectCount = 0 ;

OB1Edge::OB1Edge()
{
	_KindOf     = UNDEFINED ;
  _Class_mask = 0 ;
  _complexity = 0 ;
  _fiability  = 0 ;

  _lObjectCount++ ;
}

OB1Edge::OB1Edge(OB1EdgeType TE)
{
	_KindOf     = TE ;
  _Class_mask = 0 ;
  _complexity = 0 ;
  _fiability  = 0 ;

  _lObjectCount++ ;
}

OB1Edge::OB1Edge(OB1EdgeType ter, int clas_mask, int comple, int fiabil)
{
	_Class_mask = clas_mask ;
  _complexity = comple ;
  _fiability  = fiabil ;
  _KindOf     = ter ;

  _lObjectCount++ ;
}

OB1Edge::~OB1Edge()
{
  _lObjectCount-- ;
}

OB1EdgeType
OB1Edge::TypeOf()
{
	return (_KindOf) ;
}


void
OB1Edge::ChangeType(OB1EdgeType TE)
{
	_KindOf = TE ;
}

OB1EdgeLevel::OB1EdgeLevel(OB1EdgeType ter, int clas_mask, int comple, int fiabil, int level)
{
	_Class_mask = clas_mask ;
	_complexity = comple ;
	_fiability  = fiabil ;
	_KindOf     = ter ;
	_level      = level ;
}

